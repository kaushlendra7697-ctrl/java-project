package myjava;

import java.util.Scanner;

public class ForLoopExample {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        for (int c = 0; c < 20; c++) {
            System.out.println("hello world");
        }
    }
}
//for loop is used to perform repeated works/task
